public class FiveIntegerProduct {

  public static void main(String[] Args){
    int number1 = 11;
    int number2 = 22;
    int number3 = 33;
    int number4 = 44;
    int number5 = 55;

    int product = number1 * number2 * number3 * number4 * number5;

    System.out.println("The product of the 5 integers is " + product);
  }
}
